// Patch ảnh vào danh sách sản phẩm
export function applyImages(products) {
  return products.map(p => {
    const imgs = productImages[p.pk_product_id]
    if (!imgs) return p
    return {
      ...p,
      primary_image: imgs.primary,
      images: imgs.images.map((url, i) => ({
        pk_image_id: p.pk_product_id * 100 + i,
        image_url: url,
        is_primary: i === 0 ? 1 : 0,
        sort_order: i + 1,
      })),
    }
  })
}
